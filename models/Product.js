import mongoose from "mongoose";

const productSchema = new mongoose.Schema(
    {
        productId: {
            type: String,
            required: true,
            unique: true
        },
        name: {
            type: String,
            required: true  
        },
        altNames : {
            type: [String],//name array ekak widiyata thiyenne
            default: []
        },
        description :{
            type: String,
            required: true
        },
        price : {
            type: Number,
            required: true
        },
        labelPrice : {
            type: Number,
            required: true
        },
        images :{
            type: [String],
            required: true
        },
        category : {    
            type: String,
            required: true
        },
        model: {
            type: String,
            required: true,
            default: "standard"
        },
        brand : {
            type: String,
            required: true,
            default: "generic"
        },
        stock : {   
            type: Number,
            required: true,
            default: 0  
        },
        isAvailable : {
            type: Boolean,
            default: true
        }
    });

    const Product = mongoose.model("Product",productSchema);

export default Product;